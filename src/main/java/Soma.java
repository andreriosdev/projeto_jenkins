public class Soma {
    public static void main(String[] args) {
        // Declaração das variáveis
        int numero1 = 5;
        int numero2 = 10;

        // Cálculo da soma
        int resultado = numero1 + numero2;

        // Exibição do resultado
        System.out.println("A soma de " + numero1 + " e " + numero2 + " é: " + resultado);
    }
}
